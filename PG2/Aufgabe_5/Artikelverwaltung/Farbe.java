package artikelverwaltung;

public class Farbe {

	private int rot;
	private int blau;
	private int grün;
	
	
	
	public int getRot() {
		return rot;
	}
	public void setRot(int rot) {
		this.rot = rot;
	}
	public int getBlau() {
		return blau;
	}
	public void setBlau(int blau) {
		this.blau = blau;
	}
	public int getGrün() {
		return grün;
	}
	public void setGrün(int grün) {
		this.grün = grün;
	}
	
	
	
}
