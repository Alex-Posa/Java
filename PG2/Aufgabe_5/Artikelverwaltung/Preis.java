package artikelverwaltung;

public class Preis {
	
	private String währung;
	private double wert;
	
	
	public String getWährung() {
		return währung;
	}
	public void setWährung(String währung) {
		this.währung = währung;
	}
	public double getWert() {
		return wert;
	}
	public void setWert(double wert) {
		this.wert = wert;
	}
	
	
	
	
}
